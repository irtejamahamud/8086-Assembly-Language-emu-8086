INCLUDE 'EMU8086.INC'
.MODEL SMALL
.DATA
  A DB 50 DUP(?)
  B DB 50 DUP(?)
  C DB 50 DUP(?)

.CODE

MAIN PROC
  MOV AX,@DATA
  MOV DS,AX
 
  LEA DI,A
  MOV DX,50
  PRINT 'STRING 1: '
  CALL GET_STRING
  PRINTN ''
  LEA DI,B
  MOV DX,50
  PRINT 'STRING 2: '
  CALL GET_STRING
  PRINTN ''

  MOV SI,0
  MOV DI,0
 
  CONT1:
    MOV DL,A[SI]      
    MOV C[DI],DL
    INC SI
    INC DI
    CMP DL,0
    JE TEMP
    JNE CONT1
   
  TEMP:
    DEC DI
    MOV SI,0
    MOV C[DI],'_'
    INC DI
    JMP CONT2
   
  CONT2:
    MOV DL,B[SI]
    MOV C[DI],DL
    INC SI
    INC DI
    CMP DL,0
    JE PRINT_NEW
    JNE CONT2
   
  PRINT_NEW:
    PRINT 'TESTING C: '
    LEA SI,C
    CALL PRINT_STRING
    PRINTN ''
 
  EXIT:
    MOV AH,04CH
    INT 21H
 
MAIN ENDP

DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
DEFINE_GET_STRING
DEFINE_PRINT_STRING

END MAIN